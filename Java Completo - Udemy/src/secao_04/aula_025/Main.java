package secao_04.aula_025;

public class Main {
    public static void main(String[] args) {
        //Exemplo 1
        int x, y;
        x = 5;
        y = 2 * x;
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println();

        // Exemplo 2
        int a;
        double c;
        a = 5;
        c = 2 * a;
        System.out.println("a = " + a);
        System.out.println("c = " + c);
        System.out.println();

        // Exemplo 3
        double b, B, h, area;

        b = 6.0;
        B = 8.0;
        h = 5.0;

        area = (b + B) / 2.0 * h;
        System.out.println("Area: " + area);
        System.out.println();

        // Exemplo 4
        int d, f;
        double resultado;
        d = 5;
        f = 2;
        resultado = (double) d / f;
        System.out.println("Resultado: " + resultado);
        System.out.println();

        // Exemplo 5
        double i;
        int j;
        i = 5.0;
        j = (int) i;
        System.out.println("j = " + j);
        System.out.println();
    }
}
